const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
  email: { type: String, unique: true },
  password: String,

  role: {
    type: String,
    enum: ["user", "admin"],
    default: "user"
  },

  otp: String,
  otpExpiry: Date
}, { timestamps: true });

module.exports = mongoose.model("User", userSchema);
